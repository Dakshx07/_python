name="Daksh"

print(name[0:3])

print(name[-4:-1])  #negative slicing
print(name[1:4])    #make it opposite of negative slicing to understand the concept

print(name[:4])

print(name[1:])
print(name[1:5]) #is same as above print statement works same as above. [1:] is same as [1:len(name)]

print(name[1:5:2])  #slicing with step